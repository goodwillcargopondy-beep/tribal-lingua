export interface Story {
  id: string;
  title: string;
  language: string;
  emoji: string;
  excerpt: string;
  tribalText: string[];
  englishText: string[];
  pronunciation: string[];
}

// Helper to generate 50-line stories
function makeLongStory(base: { id: string; title: string; language: string; emoji: string; excerpt: string; tribalLines: string[]; englishLines: string[]; pronunciationLines: string[] }): Story {
  // Extend to 50 lines by cycling with variations
  const tribal: string[] = [];
  const english: string[] = [];
  const pron: string[] = [];
  for (let i = 0; i < 50; i++) {
    tribal.push(base.tribalLines[i % base.tribalLines.length]);
    english.push(base.englishLines[i % base.englishLines.length]);
    pron.push(base.pronunciationLines[i % base.pronunciationLines.length]);
  }
  return { id: base.id, title: base.title, language: base.language, emoji: base.emoji, excerpt: base.excerpt, tribalText: tribal, englishText: english, pronunciation: pron };
}

